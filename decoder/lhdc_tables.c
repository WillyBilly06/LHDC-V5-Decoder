/* ---- Helper functions ---- */

const lhdc_band_cfg_desc_t *lhdc_get_band_cfg(uint32_t sample_rate, uint8_t frame_duration_ms)
{
    /*
     * Validated against the real encoder: for HR-class streams (the default
     * negotiated), spf -> band config / MDCT size:
     *   spf=240 (44.1/48k @5ms) -> band_480_HR, MDCT 480, 240 spectral lines
     *   spf=480 (96k  @5ms)     -> band_960,    MDCT 960, 480 lines
     *   spf=960 (192k @5ms)     -> band_1920,   MDCT 1920, 960 lines
     * All use 32 bands. (band_480 / band_480_LB are non-HR variants.)
     */
    uint16_t spf = lhdc_get_samples_per_frame(sample_rate, frame_duration_ms);
    if (spf <= 240)       return &lhdc_band_cfgs[LHDC_BAND_CFG_480_HR];
    else if (spf <= 480)  return &lhdc_band_cfgs[LHDC_BAND_CFG_960];
    else                  return &lhdc_band_cfgs[LHDC_BAND_CFG_1920];
}

uint16_t lhdc_get_samples_per_frame(uint32_t sample_rate, uint8_t frame_duration_ms)
{
    return (uint16_t)((uint64_t)sample_rate * frame_duration_ms / 1000);
}

uint16_t lhdc_get_mdct_size(uint32_t sample_rate, uint8_t frame_duration_ms)
{
    /* Return the full IMDCT/MDCT length, not the hop size.
     * 96 kHz/5 ms: samples_per_frame=480 but mdct_size=960.
     * Returning the hop here can under-size buffers/tables in callers that use
     * this public helper.
     */
    const lhdc_band_cfg_desc_t *bc = lhdc_get_band_cfg(sample_rate, frame_duration_ms);
    return bc ? bc->mdct_size : lhdc_get_samples_per_frame(sample_rate, frame_duration_ms) * 2;
}

const uint32_t *lhdc_get_bitrate_table(uint32_t sample_rate)
{
    switch (sample_rate) {
        case 44100:  return lhdc_bitrate_table_44k;
        case 48000:  return lhdc_bitrate_table_48k;
        case 96000:  return lhdc_bitrate_table_96k;
        case 192000: return lhdc_bitrate_table_192k;
        default:     return lhdc_bitrate_table_48k;
    }
}

